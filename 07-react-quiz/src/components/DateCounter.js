import { useReducer, useState } from "react";
const initialState = { count: 0, step: 1 };
function reducer(state, action) {
  console.log(state, action); // if initial state of state is 0 and we pass dispatch(1) then it's output will be (state=0, action=1), next time if we call dispatch(1) output will be (state=1, action=1) bcz now state was set to 1

  // if (action.type === "inc") return state + 1;
  // if (action.type === "dec") return state - 1;
  // if (action.type === "setCount") return action.payload;

  switch (action.type) {
    case "dec":
      return { ...state, count: state.count - state.step };
    case "inc":
      return { ...state, count: state.count + state.step };
    case "setCount":
      return { ...state, count: action.payload };
    case "setStep":
      return { ...state, step: action.payload };
    case "reset":
      return initialState;
    default:
      throw new Error("Unknown action");
  }
} // reducer function takes previous state and action(which will be the value we pass in dispatch) and returns us new state
